#include <iostream>
using namespace std;
int main()
{
    //adition
    int a=9, b=8;
    int result = a + b;
    cout<<" result : "<<result<<endl;
    
    //subtraction
    result = a - b;
    cout<<" result : "<<result<<endl;
    
    //multiply
    result = a*b;
    cout<<" result : "<<result<<endl;
    
    //divide
    result = a/b;
    cout<<" result : "<<result<<endl;
   
    //modulus
    result = a%b;
    cout<<" result : "<<result<<endl;
    
    
    
    return 0;
}
